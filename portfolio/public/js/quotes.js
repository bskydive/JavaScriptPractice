function showQuote() {
    function getRandomNum(min, max) {
        return Math.floor(Math.random() * (max - min)) + min; //исключая max!!!
    }

    var authorIndBefore = getRandomNum(0, quotes.length);
    var authorInd = getRandomNum(0, quotes.length);
    //слегка загрубил ГСЧ для предотвращения повторов
    if (authorInd == authorIndBefore) {
        authorInd = getRandomNum(0, quotes.length);
    }

    var quoteIndBefore = getRandomNum(2, quotes[authorInd].length - 2);
    var quoteInd = getRandomNum(2, quotes[authorInd].length - 2); //0 - author, 1 - link
    //слегка загрубил ГСЧ для предотвращения повторов
    if (quoteInd == quoteIndBefore) {
        quoteInd = getRandomNum(2, quotes[authorInd].length - 2);
    }


    var quote = quotes[authorInd][quoteInd] + "<br /><a href='" + quotes[authorInd][1] + "'>" + quotes[authorInd][0] + "</a><br />";

    $("#id_divQuoteText").html("<p>" + quote + "</p>");

    $("#id_twitOutBtn").attr("href", "https://twitter.com/intent/tweet?text="
    + quotes[authorInd][quoteInd].substring(0, 140 - (quotes[authorInd][0].length))
    + "&hashtags=" + quotes[authorInd][0].replace(/\s+/g, ''));

};


$(document).ready(function () {

    var quotesall = 0;

    for (var i = 0; i < quotes.length - 1; i++) {
        for (var j = 0; j < quotes[i].length; j++) {
            quotesall++;
        }
    }

    $("#id_divHeaderText").html(
    " авторов: " + quotes.length.toString()
    + " цитат: " + quotesall.toString());

    $("#id_getQuoteBtn").on("click", function () {
        showQuote();
    });

    showQuote();

});
